package com.att.aro.json;

public class Score {

	private long effects;
	private long causes;
	private long total;
	
	public long getEffects() {
		return effects;
	}
	public void setEffects(long effects) {
		this.effects = effects;
	}
	public long getCauses() {
		return causes;
	}
	public void setCauses(long causes) {
		this.causes = causes;
	}
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	
	
	
}

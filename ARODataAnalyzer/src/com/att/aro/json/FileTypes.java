public class FileTypes {
	
	private String fileType;
	private double percentageOfDataTransferred;
	private long bytes;
	/**
	 * @return the fileType
	 */
	public String getFileType() {
		return fileType;
	}
	/**
	 * @param fileType the fileType to set
	 */
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	/**
	 * @return the percentageOfDataTransferred
	 */
	public double getPercentageOfDataTransferred() {
		return percentageOfDataTransferred;
	}
	/**
	 * @param percentageOfDataTransferred the percentageOfDataTransferred to set
	 */
	public void setPercentageOfDataTransferred(double percentageOfDataTransferred) {
		this.percentageOfDataTransferred = percentageOfDataTransferred;
	}
	/**
	 * @return the bytes
	 */
	public long getBytes() {
		return bytes;
	}
	/**
	 * @param bytes the bytes to set
	 */
	public void setBytes(long bytes) {
		this.bytes = bytes;
	}


}

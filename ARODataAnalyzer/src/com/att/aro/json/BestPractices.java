public class BestPractices {
	
	private String name;
	private String result;
	private BestPracticeDetails[] resultDetails;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the result
	 */
	public String getResult() {
		return result;
	}
	/**
	 * @param result the result to set
	 */
	public void setResult(String result) {
		this.result = result;
	}
	/**
	 * @return the resultDetails
	 */
	public BestPracticeDetails[] getResultDetails() {
		return resultDetails;
	}
	/**
	 * @param resultDetails the resultDetails to set
	 */
	public void setResultDetails(BestPracticeDetails[] resultDetails) {
		this.resultDetails = resultDetails;
	}
	
	
	
}
